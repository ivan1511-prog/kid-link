#ifndef VIBRO_H
#define VIBRO_H

#include <Arduino.h>

// ========================================
// Модуль управления вибромотором (DRV2605L)
// ========================================
// T-Watch-S3 использует драйвер DRV2605L через I2C.
// Поддерживает 123 встроенных эффекта вибрации.

// Типы вибрации для разных состояний
enum VibroType {
    VIBRO_SOFT,      // Мягкая — для предупреждения (FAR)
    VIBRO_STRONG,    // Сильная — для тревоги (NO_SIGNAL)
    VIBRO_ALERT      // Тревожная — несколько импульсов подряд
};

// Инициализация вибромотора (вызвать в setup())
void vibro_init();

// Включить вибрацию указанного типа
void vibro_pulse(VibroType type);

// Обновление состояния вибрации (вызвать в loop())
void vibro_update();

// Проверить, активна ли вибрация
bool vibro_is_active();

#endif // VIBRO_H