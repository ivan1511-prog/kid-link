// ========================================
// UUID пары часов
// ========================================
// Этот UUID должен быть одинаковым на обеих часах!
// Он используется для идентификации "своей" пары в BLE advertisement.
// Формат: стандартный UUID 128-bit
#define PAIR_UUID "12345678-1234-1234-1234-123456789abc"

// ========================================
// BLE настройки
// ========================================
// Интервал advertising в миллисекундах
// Меньше = чаще передаём, быстрее обнаружение, но больше расход батареи
#define BLE_ADV_INTERVAL_MS 100

// Интервал сканирования в миллисекундах
#define BLE_SCAN_INTERVAL_MS 100

// Окно сканирования (должно быть <= интервалу)
#define BLE_SCAN_WINDOW_MS 100

// ========================================
// Пороги RSSI для определения расстояния
// ========================================
// RSSI (Received Signal Strength Indicator) измеряется в dBm
// Чем ближе к 0, тем сильнее сигнал (ближе устройство)
// Типичные значения:
//   -30 dBm = очень близко (< 1м)
//   -50 dBm = близко (1-2м)
//   -70 dBm = средне (3-5м)
//   -90 dBm = далеко (> 5м)

// Порог "близко" - выше этого значения считаем что всё ОК
#define RSSI_THRESHOLD_OK -65

// Порог "далеко" - ниже этого значения считаем что слишком далеко
#define RSSI_THRESHOLD_FAR -80

// ========================================
// Таймауты
// ========================================
// Время в мс после которого считаем что сигнал потерян
#define SIGNAL_LOST_TIMEOUT_MS 3000

